function mostrartexto() {
    var textoDiv = document.getElementById('texto');
    if (textoDiv.style.display === 'none' || textoDiv.style.display === '') {
        textoDiv.style.display = 'flex';
    } else {
        textoDiv.style.display = 'none';
    }
}

